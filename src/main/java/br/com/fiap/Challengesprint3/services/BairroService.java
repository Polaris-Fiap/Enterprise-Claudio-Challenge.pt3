package br.com.fiap.Challengesprint3.services;

import java.util.List;
import java.util.Optional;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;

import br.com.fiap.Challengesprint3.models.Bairro;
import br.com.fiap.Challengesprint3.repository.BairroRepository;

@Service
public class BairroService {
    
    @Autowired
    BairroRepository repository;

    public List<Bairro> listAll() {
        return repository.findAll();
    }

    public Bairro save(Bairro bairro) {
        return repository.save(bairro);
    }

    public Optional<Bairro> getById(Long id) {
        return repository.findById(id);
    }

}
